def code_to_int(code_str: str) -> int:
    return int(code_str, 2)


def main():
    lines = []
    lines.append("// ============================================================================")
    lines.append("// JPEG 标准亮度 DC/AC Huffman 表 (generated by gen_huffman_tables.py)")
    lines.append("// 使用方式: `include \"huffman_tables.vh\"")
    lines.append("// ============================================================================")
    lines.append("")

    # DC table: 12 entries
    dc_codes = []
    dc_lens = []
    for cat in range(12):
        code_str, length = DC_TABLE.get(cat, ("0", 0))
        dc_codes.append(f"16'd{code_to_int(code_str)}")
        dc_lens.append(f"5'd{length}")

    lines.append(f"localparam [16*12-1:0] DC_HUFF_CODE = {{\n    {', '.join(reversed(dc_codes))}\n}};")
    lines.append(f"localparam [5*12-1:0]  DC_HUFF_LEN  = {{\n    {', '.join(reversed(dc_lens))}\n}};")
    lines.append("")

    # AC table: 256 entries, default len=0
    ac_codes = []
    ac_lens = []
    for sym in range(256):
        code_str, length = AC_TABLE.get(sym, ("0", 0))
        ac_codes.append(f"16'd{code_to_int(code_str)}")
        ac_lens.append(f"5'd{length}")

    # Split into chunks for readability
    lines.append("localparam [16*256-1:0] AC_HUFF_CODE = {")
    for i in range(255, -1, -1):
        suffix = "," if i != 0 else ""
        lines.append(f"    16'd{code_to_int(AC_TABLE.get(i, ('0', 0))[0])}{suffix}  // sym=0x{i:02X}")
    lines.append("};")
    lines.append("")

    lines.append("localparam [5*256-1:0] AC_HUFF_LEN = {")
    for i in range(255, -1, -1):
        suffix = "," if i != 0 else ""
        lines.append(f"    5'd{AC_TABLE.get(i, ('0', 0))[1]}{suffix}  // sym=0x{i:02X}")
    lines.append("};")
    lines.append("")

    DST.write_text("\n".join(lines), encoding="utf-8")
    print(f"Generated {DST}")


if __name__ == "__main__":
    main()
